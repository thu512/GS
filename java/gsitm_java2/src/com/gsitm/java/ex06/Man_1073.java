package com.gsitm.java.ex06;

public class Man_1073 {

	static boolean eatCheck(Lion_1073 lion) {
		if(lion.eatMeat) {
			System.out.println("사자 밥먹음");
			
		}else {
			System.out.println("사자 밥안먹음 그래서 밥줌");
			lion.eatMeat = true;
		}
		return lion.eatMeat;
	}	
	
	static boolean eatCheck1(Tiger_1073 tiger) {
		if(tiger.eatMeat) {
			System.out.println("호랑이 밥먹음");
			
		}else {
			System.out.println("호랑이 밥안먹음 그래서 밥줌");
			tiger.eatMeat = true;
		}
		return tiger.eatMeat;
	}
	public static void main(String[] args) {
		eatCheck(new Lion_1073());
		eatCheck1(new Tiger_1073());
	}

}
