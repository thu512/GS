public class RiceChk_1051 {

	public void feedCheck(Animal_1051 animal) {
		if(animal.lionchk) {
			System.out.println("사자가 밥을 먹었습니다.");
		}else {
			System.out.println("사자가 밥을 안먹었습니다.");
			animal.lionchk = true;
		}
		
		if(animal.tigerchk) {
			System.out.println("호랑이가 밥을 먹었습니다.");
		}else {
			System.out.println("호랑이가 밥을 안먹었습니다.");
			animal.tigerchk = true;
		}
	}
}
