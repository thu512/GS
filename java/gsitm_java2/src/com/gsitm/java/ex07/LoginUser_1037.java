public class LoginUser_1037 {

	private static LoginUser_1037 loginUser = new LoginUser_1037();
	private String name;
	private int empNum;
	private int auth;
	
	private LoginUser_1037() {
		this.name = null;
		this.empNum = 0;
		this.auth = 0;
		
	}
	
	static LoginUser_1037 getInstance() {
		return loginUser;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getEmpNum() {
		return empNum;
	}

	public void setEmpNum(int empNum) {
		this.empNum = empNum;
	}

	public int getAuth() {
		return auth;
	}

	public void setAuth(int auth) {
		this.auth = auth;
	}
}
