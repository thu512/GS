public class BabyCat_1032 extends MomCat_1032{
	@Override
	public void cry() {   //어미고양이 울다 재정의
		System.out.println("음메~");
	}
	public static void main(String[] args) {
		MomCat_1032 mom = new MomCat_1032();
		System.out.print("어미고양이 : ");
		mom.cry();
		//어미울다
		BabyCat_1032 baby = new BabyCat_1032();
		System.out.print("새끼고양이 : ");
		baby.cry();
		//새끼울다
	}
}
