public class ParentCat_1077 {
	private String furColor;
	public int arm;
	public int legs;
	public int shoulder;
	
	ParentCat_1077(){
		setFurColor("black");
		arm = 2;
		legs = 4;
		shoulder = 2;
	}//기본 생성자
	
	public void howl() {
		System.out.println("냐~옹");
	}//howl
	private void steal() {
		System.out.println("훔치다");
	}//steal

	//getter setter
	public String getFurColor() {
		return furColor;
	}
	public void setFurColor(String furColor) {
		this.furColor = furColor;
	}
}//class
