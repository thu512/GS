public class ScannerFor_1053 {
	public static void main(String[] args) {

		try {
			Scanner sc = new Scanner(System.in);

			int input = sc.nextInt();
			for (int i = 0; i < input; i++) {
				System.out.print("#");
			}
		} catch (InputMismatchException ime) {
			System.out.println("숫자만 입력하세요.");
		} finally {

		}
	}

}
